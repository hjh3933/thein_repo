package com.hjh.math;

public class Ex4_math {

	public static void main(String[] args) {
		// Math
		// 수학에 대한 여러 메서드 제공

		Math.max(5, 10); // 최대값
		Math.min(5, 10); // 최소값
		Math.sqrt(64); // 제곱근
		Math.abs(-4.7); // 절대값
		Math.pow(2, 8); // 제곱

		// 반올림
		Math.round(4.6); // 5 가까운정수
		Math.ceil(4.1); // 5.0 반올림
		Math.floor(4.9); // 4.0 버림

		Math.random(); // 0.0이상~1.0미만 난수
	}

}
