package com.hjh.operators;

public class Ex2_operators3 {

	public static void main(String[] args) {
		// 연산자
		System.out.println("비교연산자 \n ---------");
		int x = 5;
		int y = 3;
		// boolean 값 출력
		System.out.println(x > y);
		System.out.println(x >= y);
		System.out.println(x == y);
		System.out.println(x != y); // not 연산
	}

}
